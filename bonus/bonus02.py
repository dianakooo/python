#Напишите программу, которая переводит температуру
#из градусов Цельсия в гр. Фаренгейта и Кельвин.

Num = input("Введите температуру в градусах Цельсия: ")
if Num == "":
    print("Вы забыли ввести число!")
    raise SystemExit
cel = int(Num)
far = cel * 9 / 5 + 32
kel = cel + 273.15
print("В градусах Фаренгейта: ", far)
print("В градусах Кельвина: ", kel)
