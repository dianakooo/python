print("Привет!")

input_a=input("Введите число a: ")
a=int(input_a)

input_b=input("Введите число b: ")
b=int(input_b)

input_c=input("Введите число c: ")
c=int(input_c)

if a+b==c:
    print("Сумма a и b равна c")
else:
    print("Сумма a и b не равна c")

if a/b==c:
    print("Частное a и b равно c")
else:
    print("Частное a и b не равно c")

